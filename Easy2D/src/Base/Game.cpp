#include <easy2d/e2dbase.h>
#include <easy2d/e2dmanager.h>
#include <easy2d/e2dtool.h>


// 控制游戏终止
static bool s_bEndGame = true;
// 控制游戏暂停
static bool s_bPaused = false;
// 是否进行过初始化
static bool s_bInitialized = false;
// 游戏名称
static easy2d::String s_sGameName;


bool easy2d::Game::init(const String& title, int width, int height, const String& uniqueName)
{
	if (s_bInitialized)
	{
		E2D_WARNING("The game has been initialized!");
		return false;
	}

	if (!uniqueName.empty())
	{
		// 创建进程互斥体
		String fullMutexName = "Easy2DApp-" + uniqueName;
		HANDLE hMutex = ::CreateMutexA(nullptr, TRUE, fullMutexName.c_str());

		if (hMutex == nullptr)
		{
			E2D_WARNING("CreateMutex Failed!");
		}
		else if (::GetLastError() == ERROR_ALREADY_EXISTS)
		{
			// 如果程序已经存在并且正在运行，弹窗提示
			Window::error("游戏已在其他窗口中打开！", "提示");
			// 关闭进程互斥体
			::CloseHandle(hMutex);
			return false;
		}
	}

	// 初始化 COM 组件
	if (FAILED(CoInitialize(nullptr)))
	{
		E2D_ERROR("初始化 COM 组件失败");
		return false;
	}

	// 创建设备无关资源
	if (!Renderer::__createDeviceIndependentResources())
	{
		E2D_ERROR("渲染器设备无关资源创建失败");
		return false;
	}

	// 初始化窗口
	if (!Window::__init(title, width, height))
	{
		E2D_ERROR("初始化窗口失败");
		return false;
	}

	// 创建设备相关资源
	if (!Renderer::__createDeviceResources())
	{
		E2D_ERROR("渲染器设备相关资源创建失败");
		return false;
	}

	// 初始化 DirectInput
	if (!Input::__init())
	{
		E2D_ERROR("初始化 DirectInput 失败");
		return false;
	}

	// 初始化播放器
	if (!Music::__init())
	{
		E2D_ERROR("初始化 XAudio2 失败");
		return false;
	}

	// 初始化路径
	if (!Path::__init(title))
	{
		E2D_WARNING("Path::__init failed!");
	}

	// 保存游戏名称
	s_sGameName = title;

	// 初始化成功
	s_bInitialized = true;

	return s_bInitialized;
}

void easy2d::Game::start(int fpsLimit)
{
	if (!s_bInitialized)
	{
		E2D_ERROR("开始游戏前未进行初始化");
		return;
	}

	// 初始化场景管理器
	SceneManager::__init();
	// 显示窗口
	::ShowWindow(Window::getHWnd(), SW_SHOWNORMAL);
	// 刷新窗口内容
	::UpdateWindow(Window::getHWnd());
	// 初始化计时
	Time::__init(fpsLimit);

	s_bEndGame = false;

	while (!s_bEndGame)
	{
		// 处理窗口消息
		Window::__poll();
		// 刷新时间
		Time::__updateNow();

		// 判断是否达到了刷新状态
		if (Time::__isReady())
		{
			Input::__update();			// 获取用户输入
			Timer::__update();			// 更新定时器
			ActionManager::__update();	// 更新动作管理器
			SceneManager::__update();	// 更新场景内容
			Renderer::__render();		// 渲染游戏画面
			GC::clear();				// 清理内存

			Time::__updateLast();		// 刷新时间信息
		}
		else
		{
			Time::__sleep();			// 挂起线程
		}
	}

	// 清除运行时产生的资源
	{
		// 删除动作
		ActionManager::__uninit();
		// 回收音乐播放器资源
		MusicPlayer::__uninit();
		// 清空定时器
		Timer::__uninit();
		// 删除所有场景
		SceneManager::__uninit();
		// 清理对象
		GC::clear();
	}
}

void easy2d::Game::pause()
{
	s_bPaused = true;
}

void easy2d::Game::resume()
{
	if (s_bInitialized && s_bPaused)
	{
		Game::reset();
	}
	s_bPaused = false;
}

void easy2d::Game::reset()
{
	if (s_bInitialized && !s_bEndGame)
	{
		Time::__reset();
		ActionManager::__resetAll();
		Timer::__resetAll();
	}
}

bool easy2d::Game::isPaused()
{
	return s_bPaused;
}

void easy2d::Game::quit()
{
	s_bEndGame = true;	// 这个变量将控制游戏是否结束
}

void easy2d::Game::destroy()
{
	if (!s_bInitialized)
		return;

	// 清空图片缓存
	Image::clearCache();
	// 回收音乐相关资源
	Music::__uninit();
	// 关闭输入
	Input::__uninit();
	// 回收渲染相关资源
	Renderer::__discardResources();
	// 销毁窗口
	Window::__uninit();

	CoUninitialize();

	s_bInitialized = false;
}

easy2d::String easy2d::Game::getName()
{
	return s_sGameName;
}
